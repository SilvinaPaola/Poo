public class Radio {
    
    private double radio;
   
    
    
   //constructor vacio   
    public Radio(){
        
    }
    
    //constructor con atributo - inicializando radio(a)
    public Radio(double radio){
        this.radio= radio;
        
    }

    //getters and setters(b)
    public double getRadio() {
        return radio;
    }

    public void setRadio(double radio) {
        this.radio = radio;
    }

    @Override
    public String toString() {
        return "Radio{" + "radio=" + radio + '}';
    }

    
    
}
